const favoris = JSON.parse(localStorage.getItem("favoris")) || [];

const liste = document.getElementById("liste-favoris");

favoris.forEach(chanson => {

    const card = document.createElement("div");
    card.className = "card";
    
    card.dataset.id = chanson.id;
    card.innerHTML = `
        <h3 class="titre">${chanson.titre}</h3>
        <p class="star">${chanson.artiste}</p>

        <i class="fa-solid fa-trash trash-favori"></i>
    `;

    card.querySelector(".trash-favori").addEventListener("click", () => {

        const nouveauxFavoris = favoris.filter(
            f => f.titre !== chanson.titre
        );

        localStorage.setItem(
            "favoris",
            JSON.stringify(nouveauxFavoris)
        );

        location.reload();
    });

    liste.appendChild(card);
});

